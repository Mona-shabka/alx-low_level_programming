#include "3-calc.h"

/**
* op_add - a function that adding 2 numbers.
* @a: number 1.
* @b: number 2.
* Return: sumation result.
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - a function that substract 2 numbers.
* @a: number 1.
* @b: number 2.
* Return: substraction result.
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - a function that multiply 2 numbers.
* @a: number 1.
* @b: number 2.
* Return: multiplication result.
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - a function that divide 2 numbers.
* @a: number 1.
* @b: number 2.
* Return: division result.
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - a function that return the remaining of the division of 2 numbers.
* @a: number 1.
* @b: number 2.
* Return: the remaining of the division of 2 numbers.
*/

int op_mod(int a, int b)
{
	return (a % b);
}
